from flask import jsonify, request, Blueprint


errorBP = Blueprint('error', __name__)


@errorBP.errorhandler(404)
def not_found(error=None):
    message = {
        'status': 404,
        'message': 'Not Found: ' + request.url,
    }
    resp = jsonify(message)
    resp.status_code = 404
    return resp
